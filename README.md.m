# Rocket Lander Challenge

**Rocket Lander Challenge** is a fun and educational 2D game built with Python and Pygame. In this game, players control a rocket with two independent engines in a low-gravity environment. The goal is to safely land the rocket on a designated landing pad on a bumpy asteroid surface. Press both engine keys for full thrust, or press one to tilt the rocket and adjust your trajectory. Land too hard or miss the landing pad, and it's game over!

